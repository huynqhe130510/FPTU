   void checkAlarm() {
     String xMess; int xHo, xMi, xSe;
     Calendar u = Calendar.getInstance();
     xHo = u.get(Calendar.HOUR_OF_DAY);
     xMi = u.get(Calendar.MINUTE);
     xSe = u.get(Calendar.SECOND);
     int n,i; Alarm x,y; String s;
     n = h.size();
     for(i=0;i<n;i++) {
       x = h.get(i);
       if(x.hour==xHo && x.minute==xMi && x.second==xSe) {
        s = "" + xHo + ":";
        s += xMi + ":";
        s += xSe + "\r\n";
        s += x.message;
        JOptionPane.showMessageDialog(this, s, "Alarm information:", JOptionPane.CLOSED_OPTION); 
       }
     }
     
   }
    
