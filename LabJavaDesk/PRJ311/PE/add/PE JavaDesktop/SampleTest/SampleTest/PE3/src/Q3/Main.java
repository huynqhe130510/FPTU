    public void showOnTable(ArrayList<ProjectMember> members) {
        
        try {
            DefaultTableModel tbModel = (DefaultTableModel)jTable1.getModel();
            tbModel.setRowCount(0);
            
            if (members.isEmpty()) {
                jLabel1.setText("Not found any ProjectMember");
                return;
            }
            
            for (int i = 0; i < members.size(); i++) {
                ProjectMember proMem = members.get(i);
                int proID = proMem.getProjectID();
                Object [] data = {proID, proMem.getEmployeeID(), proMem.isIsFullTime(), proMem.getHours()};
                tbModel.addRow(data);
            }
            jLabel1.setText("Number of Employee: " + tbModel.getRowCount());
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }       
    }
