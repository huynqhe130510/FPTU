    class FontSizeThread implements Runnable {

        @Override
        public void run() {
            try {
                int count = 20;
                while (true) {
                    if (count > 50) {
                        count = 20;
                    }
                    Font f = jLabel1.getFont();
                    jLabel1.setFont(new Font(f.getName(), f.getStyle(), count));
                    jSlider1.setValue(count);
                    count += 2;
                    Thread.sleep(200);
                }
            } catch (Exception e) {
                System.out.println(e);
            }
        }
        
    }
    
