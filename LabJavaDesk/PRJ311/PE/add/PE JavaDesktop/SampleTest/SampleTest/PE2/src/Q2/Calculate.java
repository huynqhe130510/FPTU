    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jList1.getSelectedValue();
        String [] splNum = numList.split("\\s+");
        ArrayList<Integer> num = new ArrayList<>();
        for (int i = 0; i < splNum.length; i++) {
            int x = Integer.parseInt(splNum[i]);
            num.add(x);
        }
        String type;
        int resul;
        if (jRadioButton1.isSelected()) {
            type = "Sum";
            resul = 0;
            for (int i = 0; i < num.size(); i++) {
                resul += num.get(i);
            }
            
        } else {
            Collections.sort(num, new Comparator<Integer>() {
                @Override
                public int compare(Integer o1, Integer o2) {
                    return o1.intValue() - o2.intValue();
                }
            });
            if (jRadioButton2.isSelected()) {
                type = "Max";
                resul = num.get(num.size()-1);
            }
            else {
                type = "Min";
                resul = num.get(0);
            }
        }
        an.setString(name, type, resul);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

