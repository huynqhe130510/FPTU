    class MyThread1 extends Thread{
        volatile boolean isRun = false;
        
        public void execute(){
            isRun = true;
        }
        
        public void myStop(){
            isRun = false;
        }

        @Override
        public void run() {
            int low = 10, high = 100;
            
            try{
                while(true){
                    if(isRun){
                        // 1. tao so nguyen ngau nhien [low-high]
                        int num = new Random().nextInt(high-low+1) + low;
                        // 2. day so vua tao len jList
                        dlm.addElement(num);
                        
                        // nghi 1s
                        Thread.sleep(1000);
                    }
                } 
            } catch (InterruptedException ex) {
                Logger.getLogger(SlideText.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    
    class MyThread2 extends Thread{
        
        
        List<Object> listNum;

        public MyThread2(List<Object> listNum) {
            this.listNum = listNum;
        }

        public MyThread2() {
        }
        
        public void setListNum(List<Object> listNum){
            this.listNum = listNum;
        }
  
        volatile boolean isRun = false;

        public void execute(){
            isRun = true;
        }
        
        public void myStop(){
            isRun = false;
        }

        @Override
        public void run() {
            int low = 10, high = 100;
            // index : 0, 1, 2, 3, 4
            // lÃ­t   : 2, 6, 1, 9
          
            int index = 0;
            try{
                while(true){
                    if(isRun){
                        int size = listNum.size();
                        if(index >= size){
                            index = 0;
                        }
                        
                        // show num at index to jLabel
                        labelShowNum.setText(listNum.get(index).toString());
                        // tang index len 1 don vi
                        index++;
                        
                        // nghi 1s
                        Thread.sleep(1000);
                    }
                } 
            } catch (InterruptedException ex) {
                Logger.getLogger(SlideText.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
