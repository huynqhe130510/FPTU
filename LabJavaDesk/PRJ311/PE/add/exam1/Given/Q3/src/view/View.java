    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String name = jTextField1.getText();
            List<Outcome> outcomes = new OutcomeDAO().selectByShipname(name);
            for (Outcome x : outcomes) {
                Object[] data = {x.getShipName(), x.getBattleName(), x.getResult()};
                model.addRow(data);
            }
            jLabel2.setText("Number of outcome(s): " + outcomes.size());
            if (outcomes.isEmpty()) {
                jLabel2.setText("Not Found any Outcomes !");
            }

        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

