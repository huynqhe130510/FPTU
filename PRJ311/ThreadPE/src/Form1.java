    public void initThread() {
        a = new Thread() {
            public void run() {
                while (true) {
                    try {
                        sleep(2000);
                        for (int i = 0; i < arr.length; i++) {
                            sum1 = arr[0] + arr[1] + arr[2];
                            jTextField2.setText(sum1 + "");
                        }
                    } catch (Exception e) {
                    }
                }
            }
        };

        b = new Thread() {
            public void run() {
                while (true) {
                    try {
                        a.start();
                        sleep(4000);
                        for (int i = 0; i < arr.length; i++) {
                            sum2 = arr[3] + arr[4] + arr[5];
                            jTextField3.setText(sum2 + "");
                        }
                    } catch (Exception e) {
                    }
                }
            }
        };

        c = new Thread() {
            public void run() {
                while (true) {
                    try {
                        b.start();
                        sleep(6000);
                        for (int i = 0; i < arr.length; i++) {
                            sum3 = sum1 + sum2;
                            jTextField4.setText(sum3 + "");
                        }
                    } catch (Exception e) {
                    }
                }
            }
        };
        c.start();
    }

