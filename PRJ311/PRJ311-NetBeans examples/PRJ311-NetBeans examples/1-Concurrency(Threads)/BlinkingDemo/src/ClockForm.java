    void setCurrTime()
     {
      Calendar now = Calendar.getInstance();
      int h = now.get(Calendar.HOUR_OF_DAY);
      int m = now.get(Calendar.MINUTE);
      int s = now.get(Calendar.SECOND);
      String x = "" + h + ":" + m + ":" + s;
      lbl1.setText(x);
     }
