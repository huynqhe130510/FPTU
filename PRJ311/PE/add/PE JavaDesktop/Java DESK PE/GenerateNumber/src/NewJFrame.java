    class generateNumber extends Thread {

        volatile boolean isrun = false;

        public void execute() {
            isrun = true;
        }

        public void stopRun() {
            isrun = false;
        }

        @Override
        public void run() {
            try {
                while (true) {
                    if (isrun) {
                        int number = new Random().nextInt(100);
                        dlm.addElement(number + "");
                        jLabel1.setText("Now generating: " + number + "");
                        sleep(1000);
                    }
                }
            } catch (Exception e) {
            }
        }

    }

    class showThread extends Thread {

        List<String> listNum;

        public showThread() {
        }

        public void setListNum(List<String> listNum) {
            this.listNum = listNum;
        }

        volatile boolean isrun = false;

        public void execute() {
            isrun = true;
        }

        public void stopRun() {
            isrun = false;
        }

        @Override
        public void run() {
            int index = 0;
            try {
                while (true) {
                    if (isrun) {
                        int size = listNum.size();
                        if (index >= size) {
                            index = 0;
                        }
                        jLabel1.setText("Now showing: " + listNum.get(index));
                        index++;
                        sleep(1000);
                    }
                }
            } catch (Exception e) {
            }
        }

    }

