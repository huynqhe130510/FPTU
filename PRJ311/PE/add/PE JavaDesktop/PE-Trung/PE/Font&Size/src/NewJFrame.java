    void getWord() {
        String s = jTextArea1.getText();
        if (s == null || s.isEmpty()) {
            return;
        }
        String[] s1 = s.split("\\s+");
        dialog.setWord(s1.length - 1 + "");
    }

    void getLine() {
        int nonLine = 0;
        String s = jTextArea1.getText();
        String [] str = s.split("\n");
        for (int i = 0; i < str.length; i++) {
            s = str[i].trim();
            if(s.length() == 0){
                nonLine++;
            }
        }
        
        dialog.setLine(str.length - nonLine + "");
    }

    int getFontSize() {
        int size = jTextArea1.getFont().getSize();
        return size;
    }

