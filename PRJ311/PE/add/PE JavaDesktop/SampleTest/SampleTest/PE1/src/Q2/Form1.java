    public int countWords(){
        String s = jTextArea1.getText();
        String[] a = s.split("\\s");
        int count =0;
        for(String i:a){
            if(i.toUpperCase().equalsIgnoreCase("TOI")) count++;
        }
        return count;
    } 
    
