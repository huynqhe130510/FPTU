    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        m.getDataVector().removeAllElements();
        m.fireTableDataChanged();
        String s= jTextField1.getText().trim();
        if(s.isEmpty()){
            JOptionPane.showMessageDialog(this,"Blank");
            jTextField1.requestFocus();
        }
        ArrayList<ProjectMember> list = dbpm.searchByEmp(s);
        int count =list.size();
        if(jCheckBox1.isSelected() && count >=2){
            Collections.sort(list);
            m.addRow(list.get(0).toObjects());
            m.addRow(list.get(1).toObjects());
            jLabel1.setText("Number of Project member(s):2");
        }else{
            for(ProjectMember i:list)
                m.addRow(i.toObjects());
            jLabel1.setText("Number of Project member(s):"+count);
        }
        if(count==0)
            jLabel1.setText("None of Project Member has been found !");

    }//GEN-LAST:event_jButton1ActionPerformed

